// setup PPU and tables
void setup_graphics() {
  // clear sprites
  oam_clear();
  // set palette colors
  pal_all(PALETTE);
}

void main(void)
{
  setup_graphics();
  // draw message  
  vram_adr(NTADR_B(0,4));
  vram_unrle(map1);
  
  vrambuf_clear();
  set_vram_update(updbuf);
  
  // enable rendering
  ppu_on_all();
  // infinite loop
  while(1) {
  }
}
